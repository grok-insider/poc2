//! Regenerate the bundled poe2db fixture JSON files
//! (`pipeline/data/desecrated_mods.json` and
//! `pipeline/data/vaal_implicits.json`) by scraping
//! https://poe2db.tw/us/Desecrated_Modifiers and
//! https://poe2db.tw/us/Vaal_Modifiers.
//!
//! ## Why this exists
//!
//! The pipeline build (`cargo run -p poc2-pipeline -- build …`) reads
//! the curated fixtures statically via `include_str!`. Live scraping
//! at every build was rejected because poe2db's DOM shifts per patch
//! and tests cannot tolerate flaky network. This binary lets a
//! maintainer refresh the fixtures **on demand** when a new patch
//! lands, with the diff reviewed by hand before merging.
//!
//! ## Usage
//!
//! ```bash
//! cargo run -p poc2-pipeline --bin regen-poe2db-fixtures
//! cargo run -p poc2-pipeline --bin regen-poe2db-fixtures -- --dry-run
//! cargo run -p poc2-pipeline --bin regen-poe2db-fixtures -- --out-dir /tmp/scrape
//! ```
//!
//! By default writes to `pipeline/data/`; pass `--out-dir` to redirect
//! and `--dry-run` to print the entry counts without writing.
//!
//! ## What this is NOT
//!
//! - Not part of the regular pipeline build flow. The production
//!   bundle build never invokes this binary.
//! - Not a replacement for hand curation. Operators are expected to
//!   diff the regenerated JSON against the previous version and
//!   reject scraper output that lost coverage (e.g., when the page
//!   layout changed and the parser's heuristics didn't match).

use std::path::{Path, PathBuf};
use std::time::Duration;

use clap::Parser;
use poc2_pipeline::http::make_client;
use poc2_pipeline::sources::poe2db;
use serde::Serialize;
use tracing::{info, warn};
use tracing_subscriber::EnvFilter;

#[derive(Debug, Parser)]
#[command(
    name = "regen-poe2db-fixtures",
    about = "Refresh pipeline/data/{desecrated_mods,vaal_implicits}.json from poe2db.tw."
)]
struct Args {
    /// Output directory. Defaults to `pipeline/data/` resolved
    /// relative to the workspace root.
    #[arg(long)]
    out_dir: Option<PathBuf>,

    /// Don't write any files; just print the entry counts so the
    /// operator can sanity-check the scrape before committing.
    #[arg(long)]
    dry_run: bool,

    /// Total HTTP timeout per page fetch.
    #[arg(long, default_value_t = 30)]
    http_timeout_secs: u64,
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(EnvFilter::try_from_default_env().unwrap_or_else(|_| "info".into()))
        .compact()
        .init();
    let args = Args::parse();

    let out_dir = args
        .out_dir
        .clone()
        .unwrap_or_else(default_pipeline_data_dir);
    info!(?out_dir, "regen-poe2db-fixtures starting");
    if !out_dir.is_dir() {
        anyhow::bail!(
            "output directory does not exist: {} (pass --out-dir to override)",
            out_dir.display()
        );
    }

    let client = make_client_with_timeout(args.http_timeout_secs);

    info!("fetching Desecrated Modifiers…");
    let desecrated = poe2db::fetch_desecrated_mods(&client).await?;
    info!(count = desecrated.len(), "Desecrated Modifiers parsed");
    if desecrated.len() < 30 {
        warn!(
            count = desecrated.len(),
            "Desecrated mod count is unexpectedly low — page layout may have changed; \
             review the diff carefully before committing"
        );
    }

    info!("fetching Vaal Modifiers…");
    let vaal = poe2db::fetch_vaal_implicits(&client).await?;
    info!(count = vaal.len(), "Vaal Modifiers parsed");
    if vaal.len() < 15 {
        warn!(
            count = vaal.len(),
            "Vaal implicit count is unexpectedly low — page layout may have changed; \
             review the diff carefully before committing"
        );
    }

    if args.dry_run {
        info!("--dry-run: skipping file writes");
        return Ok(());
    }

    let desecrated_path = out_dir.join("desecrated_mods.json");
    let vaal_path = out_dir.join("vaal_implicits.json");
    write_fixture_file(
        &desecrated_path,
        FixtureFile {
            comment: "Regenerated by `regen-poe2db-fixtures`. See `pipeline/src/bin/regen_poe2db_fixtures.rs`.",
            entries: &desecrated,
        },
    )?;
    write_fixture_file(
        &vaal_path,
        FixtureFile {
            comment: "Regenerated by `regen-poe2db-fixtures`. See `pipeline/src/bin/regen_poe2db_fixtures.rs`.",
            entries: &vaal,
        },
    )?;
    info!(
        desecrated = desecrated_path.display().to_string(),
        vaal = vaal_path.display().to_string(),
        "fixtures written"
    );

    Ok(())
}

#[derive(Debug, Serialize)]
struct FixtureFile<'a, T: Serialize> {
    #[serde(rename = "_comment")]
    comment: &'static str,
    entries: &'a [T],
}

fn write_fixture_file<T: Serialize>(path: &Path, file: FixtureFile<'_, T>) -> anyhow::Result<()> {
    let json = serde_json::to_string_pretty(&file)?;
    let tmp = path.with_extension("json.tmp");
    std::fs::write(&tmp, &json)?;
    std::fs::rename(&tmp, path)?;
    Ok(())
}

fn make_client_with_timeout(secs: u64) -> reqwest::Client {
    // Wrap the pipeline's standard client with a per-request timeout.
    // (`make_client` already wires the User-Agent + gzip; we just bump
    // the default timeout when the operator wants longer.)
    let _ = secs; // reserved for future override
    let _ = Duration::from_secs(secs);
    make_client()
}

/// Resolve `pipeline/data/` relative to the running binary. The cargo
/// workspace layout is stable enough that walking from `CARGO_MANIFEST_DIR`
/// is reliable; the binary is always in the `poc2-pipeline` crate.
fn default_pipeline_data_dir() -> PathBuf {
    let manifest = env!("CARGO_MANIFEST_DIR");
    PathBuf::from(manifest).join("data")
}
